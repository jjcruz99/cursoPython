
print(f'Programa operadores aritmeticos')

numero1 = float(input('Ingrese un numero: '))
numero2 = float(input('Ingrese un numero: '))

resultado= numero1+numero2
print(f'{numero1} + {numero2} = {resultado}\n')

resultado= numero1 - numero2
print(f'{numero1} - {numero2} = {resultado}\n')

resultado=numero1 * numero2
print(f'{numero1} * {numero2} = {resultado}\n')

resultado=numero1 / numero2
print(f'Division {numero1} / {numero2} = {resultado}\n')

resultado=numero1 // numero2
print(f'Parte entera {numero1} // {numero2} = {resultado}\n')

resultado=numero1 % numero2
print(f'Modulo {numero1} % {numero2} = {resultado}\n')

resultado=numero1 ** numero2
print(f'Exponencial {numero1} ** {numero2} = {resultado}\n')

